import { Card, CardUpdate } from '@/lib/types/card';

const DIMENSIONS = ['who', 'what', 'when', 'where'] as const;

const EXCERPT_MAX_LENGTH = 150;

/**
 * Quote card attribution for feed/detail: prefer `subtitle`, else `excerpt`.
 * Prefixes an em dash when the string does not already start with a dash character.
 */
export function formatQuoteAttribution(
  subtitle: string | null | undefined,
  excerpt: string | null | undefined
): string {
  const raw = subtitle?.trim() || excerpt?.trim() || '';
  if (!raw) return '';
  if (/^[\u2014\u2013\u2012\u2015\-–—]\s*/.test(raw)) return raw;
  return `\u2014 ${raw}`;
}

/** Strips HTML tags and entities, returning plain text. */
export function stripHtml(html: string): string {
  return html
    .replace(/<[^>]*>/g, ' ')
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/\s+/g, ' ')
    .trim();
}

/** Generates an excerpt from HTML content by stripping tags and truncating at a word boundary. */
export function generateExcerpt(html: string | null | undefined, maxLength = EXCERPT_MAX_LENGTH): string {
  if (!html) return '';
  const plain = stripHtml(html);
  if (plain.length <= maxLength) return plain;
  const truncated = plain.slice(0, maxLength);
  const lastSpace = truncated.lastIndexOf(' ');
  return (lastSpace > maxLength * 0.6 ? truncated.slice(0, lastSpace) : truncated) + '…';
}

/**
 * Groups collection cards by dimension (who, what, when, where).
 * A collection appears in every dimension group it has tags for (Option B).
 * Uncategorized: collections with no tags in any dimension.
 * Within each group, sorted by title A-Z.
 */
export function groupCollectionsByDimension(cards: Card[]): Record<string, Card[]> {
  const groups: Record<string, Card[]> = {
    who: [],
    what: [],
    when: [],
    where: [],
    uncategorized: [],
  };

  for (const card of cards) {
    const hasAnyTag =
      (card.who?.length ?? 0) > 0 ||
      (card.what?.length ?? 0) > 0 ||
      (card.when?.length ?? 0) > 0 ||
      (card.where?.length ?? 0) > 0;

    if (!hasAnyTag) {
      groups.uncategorized.push(card);
      continue;
    }

    for (const dim of DIMENSIONS) {
      const tags = card[dim];
      if (tags && tags.length > 0) {
        groups[dim].push(card);
      }
    }
  }

  // Sort each group by title A-Z
  for (const key of Object.keys(groups)) {
    groups[key].sort((a, b) => {
      const titleA = (a.title || a.subtitle || 'Untitled').toLowerCase();
      const titleB = (b.title || b.subtitle || 'Untitled').toLowerCase();
      return titleA.localeCompare(titleB);
    });
  }

  return groups;
}
